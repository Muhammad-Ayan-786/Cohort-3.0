/* 
Types of Loops :
1. Entry Controlled Loop :
  Always check condition, and execute
  the loop body.
  Eg : for , while loop

2. Exit Controlled Loop :
  Run one time at least, then check
  condition for other executions.
  Eg : do-while loop
*/

let n = 5

for (let i = 1; i <= n; i++) {
  console.log("Hello World");
}